<script>
import SEO from "seo";
import WEBFLOW_JSON from 'assets/json/webflow/landing-page.json';
import WhatIsMuso from 'components/landing/what-is-muso';
import mobileRouter from 'services/mobile-app-router';

export default {
    name: 'WebflowPage',
    components: {
      WhatIsMuso,
    },
    created() {
      if (!this.$isServer) {
        const code = this.$route.query.code;

        if (code) {
          this.confirmEmail({ code });
        } else {
          this.redirectToAppIfNeeded();
        }
      }
    },
    mounted() {
      // Set webflow page id
      document.documentElement.setAttribute('data-wf-page', '5efe59c60b4ff03d5bf9a19b');

      // Initialize webflow data
      Webflow.require('ix2').init(WEBFLOW_JSON);
    },
    computed: {
      seo() {
        return SEO;
      }
    },
    methods: {
      async confirmEmail({ code }) {
        try {
          await this.$store.models.auth.confirmEmail({ code });

          if (this.$deviceDetector.mobile) {
            mobileRouter.push({ name: 'email-confirmed' });
          } else {
            this.$notify('Your email has been confirmed successfully.', 'info');
          }
        } catch (error) {
          this.$notify(error.message || 'Whoops! Something went wrong.', 'error');
        }
      },
      getUrlFragment() {
        return location ? location.hash.substring(1) : null;
      },
      redirectToAppIfNeeded() {
        const appRoute = this.getUrlFragment();

        if (appRoute && this.$deviceDetector.mobile) {
          mobileRouter.push(appRoute);
        }
      }
    },
};
</script>

